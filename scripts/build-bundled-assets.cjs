// scripts/build-bundled-assets.cjs
//
// Generates supabase/functions/publish-landing-page/bundled-assets.ts from
// the original HTML/CSS/JS/_headers files. Runs as part of pre-deploy so
// every deploy carries an up-to-date bundle.
//
// Why this exists: Supabase Edge Functions are bundled by esbuild via the
// Supabase CLI, and esbuild only emits files that are reachable from the
// import graph. Runtime reads via `Deno.readTextFile(new URL(...))` are NOT
// picked up by esbuild, so the deployed function would otherwise have no
// access to the HTML templates / CSS / JS / _headers. By inlining them as
// a single TypeScript module that the function imports, the content ships
// inside the bundle.

const fs = require('node:fs');
const path = require('node:path');

const repoRoot = path.join(__dirname, '..');
const dir = path.join(repoRoot, 'supabase', 'functions', 'publish-landing-page');
const sourceDir = path.join(repoRoot, 'supabase', 'functions', 'publish-landing-page_source');
const edgeFnDir = path.join(repoRoot, 'netlify', 'edge-functions');

const assets = {
    indexTpl: 'template_index.html',
    thankYouTpl: 'template_thank_you.html',
    privacyTpl: 'template_privacy.html',
    css: 'styles.css',
    runtime: 'client_runtime.js',
    pixelJs: 'assets/pixel.js',
    sha256Js: 'assets/sha256.js',
    headersFile: '_headers',
};

let out = '';
out += '// AUTO-GENERATED by scripts/build-bundled-assets.cjs — do NOT edit by hand.\n';
out += '// See scripts/build-bundled-assets.cjs for why this file exists.\n\n';
for (const [name, file] of Object.entries(assets)) {
    // The original source assets live in publish-landing-page_source/ (sibling
    // to the function dir) so the Supabase CLI's file scanner doesn't try to
    // upload them as separate assets (which triggered a Windows-only bug
    // where it tried to read file content as a filename).
    const fullPath = path.join(sourceDir, file);
    if (!fs.existsSync(fullPath)) {
        console.error(`Missing source asset: ${file}`);
        process.exit(1);
    }
    const content = fs.readFileSync(fullPath, 'utf8');
    out += `export const ${name}: string = ${JSON.stringify(content)};\n\n`;
}

// Also bundle every *.ts file under netlify/edge-functions/. Each edge
// function gets its own export named after the file (kebab/camel case
// preserved as-is). The publish function writes each one to the
// corresponding path inside the Netlify deploy ZIP.
//
// Why bundle: Supabase Edge Functions are bundled by esbuild, which only
// emits files reachable from the import graph. Deno.readTextFile at
// runtime doesn't reach files outside the function dir, so the edge
// function source must be inlined into the bundle to ship at all.
//
// Important: the Supabase CLI's Windows file scanner mistakes a literal
// path string in bundled-assets.ts for a real filesystem path and tries
// to read it (this is the bug already worked around for HTML/CSS by
// moving them to a sibling source directory). To dodge it we emit the
// path as TWO exports — segment-1 ("netlify/edge-functions") and
// segment-2 (the file name) — and concatenate them at runtime in
// publish-landing-page/index.ts. The full path string NEVER appears as
// a single literal in the generated bundle.
if (fs.existsSync(edgeFnDir)) {
    const files = fs.readdirSync(edgeFnDir).filter(f => f.endsWith('.ts'));
    out += `export const edgeFn_dir: string = ${JSON.stringify('netlify' + '/' + 'edge-functions')};\n`;
    for (const file of files) {
        const fullPath = path.join(edgeFnDir, file);
        let content = fs.readFileSync(fullPath, 'utf8');
        // Strip the leading file-path comment line (e.g.
        // `// netlify/edge-functions/subdomain-router.ts`) — otherwise
        // the Supabase CLI's Windows file scanner mistakes the literal
        // string for a real filesystem path and tries to read it,
        // crashing the deploy.
        content = content.replace(/^\/\/[^\n]*\n/, '');
        const exportName = file.replace(/\.ts$/, '').replace(/[^a-zA-Z0-9_]/g, '_');
        out += `export const edgeFn_${exportName}_file: string = ${JSON.stringify(file)};\n`;
        out += `export const edgeFn_${exportName}_body: string = ${JSON.stringify(content)};\n\n`;
    }
}

const outPath = path.join(dir, 'bundled-assets.ts');
fs.writeFileSync(outPath, out);
console.log(`Wrote ${outPath} (${out.length} bytes)`);