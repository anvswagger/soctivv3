Deno.serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }
  if (req.method !== 'POST') {
    return new Response(JSON.stringify({ error: 'Method not allowed' }), {
      status: 405,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }

  try {
    const supabaseUrl = Deno.env.get('SUPABASE_URL')!;
    const supabaseServiceKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!;
    const supabase = createClient(supabaseUrl, supabaseServiceKey);

    // ─── Parse input
    let body: { landing_page_id?: string; dry_run?: boolean };
    try {
      body = await req.json();
    } catch {
      return new Response(JSON.stringify({ error: 'Invalid JSON body' }), {
        status: 400,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      });
    }
    const landingPageId = body.landing_page_id;
    const dryRun = !!body.dry_run;
    if (!landingPageId) {
      return new Response(JSON.stringify({ error: 'Missing landing_page_id' }), {
        status: 400,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      });
    }

    // ─── Fetch page + config
    // Pull both `subdomain` and `custom_domain` so we can compute the
    // external published URL (clean <brand>.soctiv.ly, not the bare Netlify
    // deploy URL with a <pageId>/ subfolder).
    const { data: page, error: pageError } = await supabase
      .from('landing_pages')
      .select('id, client_id, product_id, product_dna_id, status, config, subdomain, custom_domain')
      .eq('id', landingPageId as any)
      .single();

    if (pageError || !page) {
      return new Response(JSON.stringify({ error: 'Landing page not found' }), {
        status: 404,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      });
    }
    const config: any = (page as any).config;
    if (!config || typeof config !== 'object' || Object.keys(config).length === 0) {
      return new Response(JSON.stringify({ error: 'Landing page has no config. Generate it first.' }), {
        status: 400,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      });
    }

    // ─── Resolve the external published URL (security/external-feel fix)
    //
    // Priority:
    //   1. custom_domain  → https://<custom_domain>            (the user's own DNS)
    //   2. subdomain      → https://<subdomain>.soctiv.ly      (our wildcard)
    //   3. fallback       → <deployUrl>/<pageId>/              (Netlify, ugly but works)
    //
    // Before this fix, `published_url` was the bare Netlify deploy URL —
    // opening "View live" in the editor landed on a Netlify site page
    // (often a 404) and the privacy link resolved to a path that included
    // the internal page id, which felt like an internal route rather
    // than an external landing page.
    //
    // NOTE: this URL is what the runtime templates see via
    // `{{publishedBaseUrl}}` (used for the privacy-policy absolute link).
    // It is ALSO what we write back to `landing_pages.published_url`,
    // so the editor's "View live" button + the published card both
    // open the right page.
    const customDomain = (page as any).custom_domain
      ? String((page as any).custom_domain).trim().toLowerCase()
      : '';
    const subdomain = (page as any).subdomain
      ? String((page as any).subdomain).trim().toLowerCase()
      : '';
    // Stash on config so the template substitution at the bottom can
    // pick it up without threading it through every helper.
    (config as any).__resolvedSubdomain = subdomain;
    (config as any).__resolvedCustomDomain = customDomain;

    // ─── Stamp webhook + tracking defaults
    if (!config.webhook) config.webhook = {};
    if (!config.webhook.url) {
      config.webhook.url = `${supabaseUrl}/functions/v1/facebook-leads-webhook`;
    }
    if (!config.webhook.thankYouUrl) config.webhook.thankYouUrl = 'thank-you.html';
    if (!config.webhook.source) config.webhook.source = 'Landing Page';
    if (!config.webhook.clientCode) {
      const { data: client } = await supabase
        .from('clients')
        .select('webhook_code')
        .eq('id', (page as any).client_id as any)
        .single();
      if (client?.webhook_code) config.webhook.clientCode = client.webhook_code;
    }
    if (!config.webhook.productCode) {
      const { data: product } = await supabase
        .from('products')
        .select('code')
        .eq('id', (page as any).product_id as any)
        .single();
      if (product?.code) config.webhook.productCode = product.code;
    }
    if (!config.tracking) config.tracking = {};
    if (!config.tracking.capiUrl) {
      config.tracking.capiUrl = `${supabaseUrl}/functions/v1/capi-proxy`;
    }
    if (typeof config.tracking.debug !== 'boolean') config.tracking.debug = false;

    // ─── Tracking field normalization
    // Defensive: a pixelId of '0' is truthy by === but treated as falsy by
    // the templating engine's `isTruthy` (0 === 0 check at templating.ts:194),
    // which means `{{#if tracking.pixelId}}` would silently skip the tracking
    // scripts and the user would have no idea why their pixel isn't firing.
    // Same risk for whitespace or non-digit characters that survived the
    // editor's `replace(/\D/g, '')` filter. Normalize here so the template
    // sees a clean string-or-empty value, and the conditional works as
    // expected.
    if (typeof config.tracking.pixelId === 'string') {
        const cleaned = config.tracking.pixelId.replace(/\D/g, '');
        // Meta pixel IDs are 10-20 digits; anything outside that is invalid
        // and we treat as "pixel disabled" (the tracking scripts are omitted).
        if (cleaned === '0' || (cleaned.length > 0 && (cleaned.length < 10 || cleaned.length > 20))) {
            config.tracking.pixelId = '';
        } else {
            config.tracking.pixelId = cleaned;
        }
    } else {
        config.tracking.pixelId = '';
    }

    // ─── Theme defaults
    if (!config.theme) config.theme = { palette: DEFAULT_PALETTE, font: 'Alexandria' };
    if (!SOCTIV_PALETTES[config.theme.palette]) config.theme.palette = DEFAULT_PALETTE;
    // Keep `config.theme.font` as the user-picked name (e.g. "IBM Plex Sans
    // Arabic", "Readex Pro"). The previous code overwrote it with the URL-safe
    // form ("IBM+Plex+Sans+Arabic") via `fontName`, but `FONT_STACK` keys are
    // the human-readable names — so `fontStack(theme)` fell back to Alexandria
    // for any font whose name contained a space. The URL-safe form is not
    // actually needed anywhere (the template hardcodes the Google Fonts <link>
    // with all 8 families), so the rewrite was just breaking the stack lookup.

    // ─── Form copy defaults
    if (!config.form) config.form = {};
    Object.assign(config.form, FORM_COPY_DEFAULTS);

    // ─── Pricing copy defaults
    if (!config.pricing) config.pricing = {};
    Object.assign(config.pricing, PRICING_COPY_DEFAULTS);

    // ─── Business defaults
    if (!config.business) {
      config.business = { brand: 'soctiv', copyright: 'جميع الحقوق محفوظة', supportEmail: '', privacyEmail: '' };
    }
    if (!config.business.brand) config.business.brand = 'soctiv';
    if (!config.business.brandInitial) {
      config.business.brandInitial = String(config.business.brand || 's').charAt(0);
    }
    // The footer template hardcodes the `© <year> <brand> —` prefix
    // (see template_index.html:339 / template_thank_you.html:169). Stamp
    // only the trailing phrase so the prefix doesn't render twice.
    if (!config.business.copyright) {
      config.business.copyright = 'جميع الحقوق محفوظة';
    }

    // ─── SEO defaults
    if (!config.seo) config.seo = {};
    if (!config.seo.year) config.seo.year = String(new Date().getFullYear());
    if (!config.seo.title && config.product?.nameArabic) {
      config.seo.title = `${config.product.nameArabic} — الدفع عند الاستلام | ${config.business.brand}`;
    }
    if (!config.seo.description && config.hero?.subline) {
      config.seo.description = String(config.hero.subline).slice(0, 160);
    }

    // ─── Trust defaults — Phase 5: migrate legacy `string[]` shape to the
    // new `{ enabled, items }` strip. Same logic as
    // src/services/soctivLandingPreview.ts `buildPreviewContext`.
    const trustStrip = (
        v: string[] | { enabled?: boolean; items: string[] } | undefined,
        fallback: string[]
    ): { enabled: boolean; items: string[] } => {
        if (Array.isArray(v)) {
            return {
                enabled: true,
                items: v.length > 0 ? v.slice(0, 6) : fallback,
            };
        }
        const obj = v || {};
        return {
            enabled: obj.enabled !== false,
            items: Array.isArray(obj.items) && obj.items.length > 0
                ? obj.items
                : fallback,
        };
    };
    if (!config.trust) {
        config.trust = {
            badges: trustStrip(undefined, ['الدفع عند الاستلام', 'توصيل مجاني', 'ضمان سنة']),
            row: trustStrip(undefined, ['دفع عند الاستلام', 'توصيل مجاني']),
        };
    } else {
        config.trust = {
            badges: trustStrip(config.trust.badges as any, ['الدفع عند الاستلام', 'توصيل مجاني', 'ضمان سنة']),
            row: trustStrip(config.trust.row as any, ['دفع عند الاستلام', 'توصيل مجاني']),
        };
    }

    // ─── Reviews defaults — Phase 5 toggle (default ON for legacy configs)
    if (!config.reviews) {
        config.reviews = { enabled: true, heading: 'ماذا يقول عملاؤنا', subheading: '', items: [] };
    } else {
        config.reviews = { ...config.reviews, enabled: config.reviews.enabled !== false };
    }

    // ─── Templates + assets
    // All inlined via `bundled-assets.ts` so the esbuild bundler ships
    // them inside the function bundle. See the comment in that file for
    // why we can't use Deno.readTextFile here.
    //
    // The two legacy variables (`pixelJs`, `sha256Js`) used to be `let`
    // with try/catch fallbacks. Now they're guaranteed to be non-empty
    // strings from the import (build script errors out if the source
    // files are missing), so we keep the same name without the fallback.
    void pixelJs;
    void sha256Js;

    // ─── Build the template context
    // `__publishedBaseUrl` is the absolute origin (no trailing slash) of
    // the live page. Templates use it to build absolute links for the
    // privacy policy + back-to-home + og:url — these used to be relative
    // hrefs, which resolved to the bare Netlify deploy URL with the
    // internal page-id subfolder visible in the address bar. Absolute
    // URLs make the published page feel like an external site (e.g.
    // https://my-brand.soctiv.ly/privacy-policy.html) and keep the
    // user inside the same origin when they click around.
    //
    // If the page has a subdomain or custom_domain, we know the final
    // URL up-front and can render with the absolute base right away.
    // If neither is set, we render with an empty placeholder, deploy,
    // and re-deploy with the real Netlify URL — see the two-pass block
    // further down.
    // We always know the final external URL up front now (Storage serving is
    // deterministic — no two-pass "deploy then learn the URL" dance):
    //   custom_domain → https://<custom_domain>
    //   subdomain     → https://<subdomain>.soctiv.ly
    //   neither       → https://soctiv.ly/p/<pageId>/   (path fallback, still
    //                   reachable via the same subdomain-router edge function)
    const knownBaseUrl = customDomain
      ? `https://${customDomain}`
      : subdomain
        ? `https://${subdomain}.${LANDING_BASE_DOMAIN}`
        : `https://${LANDING_BASE_DOMAIN}/p/${landingPageId}`;

    // ─── Per-page HMAC token (defense in depth against conversion fraud).
    // The capi-proxy requires this header to be a valid HMAC-SHA256 of the
    // page id using CAPI_SHARED_SECRET. We compute it here so the secret
    // never reaches the browser (only the per-page token does).
    const capiSecret = Deno.env.get('CAPI_SHARED_SECRET') || '';
    let pageToken = '';
    if (capiSecret) {
      try {
        const enc = new TextEncoder();
        const key = await crypto.subtle.importKey(
          'raw', enc.encode(capiSecret),
          { name: 'HMAC', hash: 'SHA-256' }, false, ['sign']
        );
        const sigBytes = await crypto.subtle.sign('HMAC', key, enc.encode(landingPageId));
        pageToken = 'sha256=' + [...new Uint8Array(sigBytes)]
          .map((b) => b.toString(16).padStart(2, '0')).join('');
      } catch (e) {
        console.warn('page token generation failed (capi-proxy will reject if it requires auth):', (e as Error).message);
      }
    }

    const ctx: TemplateContext = {
      ...config,
      __cssVars: paletteToCssVars(config.theme),
      // Pre-built inlined tracking-config script. See buildTrackConfigScript
      // for why this is server-built and not templated field-by-field.
      __trackConfigScript: buildTrackConfigScript(config.tracking, config.product, pageToken, landingPageId),
      __publishedBaseUrl: knownBaseUrl,
      // The page id flows into the runtime via the SOCTIV_CONFIG script so
      // pixel.js can include it in CAPI POSTs (the proxy verifies the
      // signature against this id).
      __pageId: landingPageId,
    };

    // ─── Render the three page templates with the inlined config script.
    // `injectBeforeRuntime` is shared between the render and any later
    // re-render (see the two-pass block below) — it splices the runtime
    // config right before <script src="runtime.js"></script> so the
    // runtime sees the config on first eval.
    const renderAll = (baseUrl: string): {
        indexHtml: string;
        thankYouHtml: string;
        privacyHtml: string;
    } => {
        const localCtx = { ...ctx, __publishedBaseUrl: baseUrl };
        const configJson = JSON.stringify({
          product: config.product,
          pricing: {
            tiers: config.pricing?.tiers || [],
            maxQty: config.pricing?.maxQty || 5,
            discountLabel: config.pricing?.discountLabel || 'التخفيض',
          },
          form: config.form,
          webhook: config.webhook,
          tracking: { debug: !!config.tracking?.debug },
        }).replace(/<\/script>/gi, '<\\/script>');
        const configScript = `<script>window.__SOCTIV_CONFIG__ = ${configJson};<\/script>\n`;
        const injectBeforeRuntime = (html: string): string => {
            // Function-callback replacement, NOT a string. The config contains
            // user-controlled strings (product name, headline, CTA text, …).
            // If any of them happens to contain `$&` (e.g. a literal `$&` in a
            // headline), `String.replace` would interpret it as "the matched
            // substring" — replacing it with `<script src="runtime.js"></script>`
            // or `</body>` (whichever matched) — and leak the literal HTML tag
            // into the page body as visible text. The function form short-circuits
            // the interpretation. Same fix as
            // src/services/soctivLandingPreview.ts `injectConfigScript`.
            if (html.includes('<script src="runtime.js"></script>')) {
                return html.replace(
                    '<script src="runtime.js"></script>',
                    () => configScript + '<script src="runtime.js"></script>'
                );
            }
            return html.replace('</body>', () => configScript + '</body>');
        };
        return {
            indexHtml: injectBeforeRuntime(renderTemplate(indexTpl, localCtx)),
            thankYouHtml: injectBeforeRuntime(renderTemplate(thankYouTpl, localCtx)),
            privacyHtml: renderTemplate(privacyTpl, localCtx),
        };
    };

    // ─── Dry run: return the rendered HTML
    if (dryRun) {
      const dry = renderAll(knownBaseUrl);
      return new Response(
        JSON.stringify({
          dry_run_html: dry.indexHtml,
          index_length: dry.indexHtml.length,
          thank_you_length: dry.thankYouHtml.length,
          privacy_length: dry.privacyHtml.length,
          published_base_url: knownBaseUrl || null,
        }),
        { status: 200, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    // ─── Publish target: a PUBLIC Supabase Storage bucket.
    // The rendered files are uploaded here and served on the fly by the
    // `subdomain-router` Netlify edge function. No Netlify token, no zip,
    // no deploy collision with the CRM app's git build (see the file header).
    const BUCKET = 'landing-pages';
    // Ensure the bucket exists + is public. Idempotent: only the first ever
    // publish actually creates it; afterwards createBucket returns an
    // "already exists" error we intentionally swallow.
    try {
      const { error: bucketErr } = await supabase.storage.createBucket(BUCKET, {
        public: true,
        fileSizeLimit: '5MB',
      });
      if (bucketErr && !/exist/i.test(bucketErr.message || '')) {
        console.warn('createBucket warning:', bucketErr.message);
      }
    } catch (e) {
      console.warn('createBucket threw:', (e as Error).message);
    }

    // Each page lives under its own `<pageId>/` prefix in the bucket. The
    // edge function rewrites `<sub>.soctiv.ly/<path>` → `<pageId>/<path>`,
    // so the relative asset refs in the rendered HTML (styles.css, runtime.js,
    // thank-you.html, …) resolve correctly without any per-page rewriting.
    const pageId = (page as any).id as string;

    // Render the three pages once. We already know the final external URL
    // (subdomain / custom domain / /p/<id> fallback) so there's no two-pass
    // deploy dance anymore.
    const rendered = renderAll(knownBaseUrl);

    const HTML = 'text/html; charset=utf-8';
    const CSS = 'text/css; charset=utf-8';
    const JS = 'application/javascript; charset=utf-8';
    const files: Array<{ path: string; body: string; type: string }> = [
      { path: `${pageId}/index.html`, body: rendered.indexHtml, type: HTML },
      { path: `${pageId}/thank-you.html`, body: rendered.thankYouHtml, type: HTML },
      { path: `${pageId}/privacy-policy.html`, body: rendered.privacyHtml, type: HTML },
      { path: `${pageId}/styles.css`, body: css, type: CSS },
      { path: `${pageId}/runtime.js`, body: runtime, type: JS },
    ];
    if (pixelJs) files.push({ path: `${pageId}/pixel.js`, body: pixelJs, type: JS });
    if (sha256Js) files.push({ path: `${pageId}/sha256.js`, body: sha256Js, type: JS });

    // Upload every file with upsert so re-publishing overwrites in place.
    // cacheControl is short (60s) so edits go live quickly. Any failure
    // aborts the publish — a half-uploaded page (HTML present but runtime.js
    // missing) is worse than a clear error the user can retry.
    const uploadOne = async (path: string, body: string, type: string) => {
      const { error } = await supabase.storage
        .from(BUCKET)
        .upload(path, new Blob([body], { type }), {
          contentType: type,
          upsert: true,
          cacheControl: '60',
        });
      if (error) throw new Error(`Failed to upload ${path}: ${error.message}`);
    };
    for (const f of files) {
      await uploadOne(f.path, f.body, f.type);
    }

    // ─── Rebuild the subdomain → pageId map the edge router reads.
    // The `subdomain-router` edge function fetches `_map.json` to resolve
    // `<sub>.soctiv.ly` → `<pageId>`, then serves `<pageId>/<path>` from this
    // bucket. This single file is the WHOLE routing table, so it must list
    // EVERY published page that has a subdomain — not just the one we're
    // publishing now. We add the current page explicitly because its row may
    // still say 'draft' until the update below commits.
    const { data: allPublishedPages, error: allPubError } = await supabase
      .from('landing_pages')
      .select('id, subdomain')
      .not('subdomain', 'is', null)
      .neq('subdomain', '')
      .eq('status', 'published' as any);
    if (allPubError) {
      console.warn('Failed to query published pages for the subdomain map:', allPubError.message);
    }
    const subdomainMap: Record<string, string> = {};
    if (subdomain) subdomainMap[subdomain] = pageId;
    for (const p of (allPublishedPages || []) as Array<{ id: string; subdomain: string }>) {
      const s = String(p.subdomain || '').trim().toLowerCase();
      if (s && !subdomainMap[s]) subdomainMap[s] = String(p.id);
    }
    await uploadOne(
      '_map.json',
      JSON.stringify({ generated_at: new Date().toISOString(), map: subdomainMap }, null, 2) + '\n',
      'application/json; charset=utf-8'
    );

    // ─── Canonical external URL the user shares / opens. We already know it
    // (knownBaseUrl) because Storage serving is deterministic — no two-pass
    // "deploy, learn the URL, re-deploy" dance needed anymore.
    const publishedBaseUrl = knownBaseUrl;
    const publishedUrl = `${publishedBaseUrl}/`;

    // ─── Update the row
    const publishedAt = new Date().toISOString();
    const { error: updateError } = await supabase
      .from('landing_pages')
      .update({
        status: 'published',
        published_at: publishedAt,
        published_url: publishedUrl
      } as any)
      .eq('id', pageId as any);
    if (updateError) {
      // Upload succeeded but the DB update failed — still return the URL so
      // the user can open the live page; the row just won't show "published".
      console.warn('Failed to update landing_pages row:', updateError.message);
    }

    return new Response(
      JSON.stringify({
        published_url: publishedUrl,
        published_base_url: publishedBaseUrl,
        published_at: publishedAt,
        storage_bucket: BUCKET,
        // Surface what the templates substituted so a debug dry-run can
        // inspect the value without scraping the rendered HTML.
        resolved: {
          subdomain,
          custom_domain: customDomain,
        }
      }),
      { status: 200, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (err) {
    console.error('Publish error:', err);
    return new Response(
      JSON.stringify({ error: (err as Error)?.message || 'Internal server error' }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
