/** Resolve a Soctiv config theme into CSS custom-property declarations
 *  wrapped in `:root { ... }`. Emits both the palette tokens AND a
 *  `--font` declaration so the rendered HTML always carries the user's
 *  font choice in the first slot of the stack. Without `--font` in this
 *  block, the body fell back to whichever family was hardcoded first in
 *  `styles.css` (the old "Alexandria never renders" bug). */
function paletteToCssVars(theme: any): string {
    const paletteKey = theme?.palette && SOCTIV_PALETTES[theme.palette]
        ? theme.palette
        : DEFAULT_PALETTE;
    const vars = SOCTIV_PALETTES[paletteKey];
    const body = Object.entries(vars)
        .map(([k, v]) => `${k}: ${v}`)
        .join('; ');
    return `:root { ${body}; --font: ${fontStack(theme)}; }`;
}

/** Resolve the Google Fonts URL-safe font name for the `theme.font` value. */
function fontName(theme: any): string {
    return FONT_MAP[theme?.font] || 'Alexandria';
}

function fontStack(theme: any): string {
    return FONT_STACK[theme?.font] || FONT_STACK['Alexandria'];
}

// ─── Tracking config script builder ─────────────────────────────────────────

/**
 * Build the inlined `<script>window.SOCTIV_TRACK_CONFIG = {...};</script>`
 * block that template_index.html injects before sha256.js + pixel.js.
 *
 * Why this lives here and not in the template: the raw value contains
 * user-controlled strings (capiUrl, testEventCode, product name) that
 * would otherwise be substituted via `{{{...}}}` in the template — but
 * `{{{...}}}` is a raw, un-escaped substitution, so a malicious or
 * paste-of-`</script>` value would break out of the inlined script tag
 * and run arbitrary code in the published page (an XSS sink affecting
 * every page with the user's domain).
 *
 * The safe pattern is:
 *   1. JSON.stringify encodes every user value as a JS literal — quotes,
 *      backslashes, and control chars are escaped automatically.
 *   2. The only sequence the HTML parser's raw-text script state treats
 *      as a terminator is `</script>` (case-insensitive). We escape that
 *      specifically — both the original and the JS-escaped form `<\/script>`
 *      that the parser would otherwise pass through as data but the runtime
 *      would re-emit on a future JSON.stringify round-trip.
 *
 * Returns the literal script block, ready to drop into the rendered HTML.
 * Always returns a valid script even if tracking is disabled — but the
 * caller is responsible for gating this behind `{{#if tracking.pixelId}}`
 * so a no-op script isn't emitted.
 */
function buildTrackConfigScript(tracking: any, product: any, pageToken: string = '', pageId: string = ''): string {
    const safeConfig = {
        pixelId: String(tracking?.pixelId || ''),
        capiUrl: String(tracking?.capiUrl || ''),
        testEventCode: String(tracking?.testEventCode || ''),
        debug: !!tracking?.debug,
        // Per-page HMAC token — closes the conversion-fraud vector where
        // anyone with the capiUrl can POST fake Lead/Purchase events. The
        // capi-proxy recomputes HMAC(CAPI_SHARED_SECRET, page_id) and
        // verifies it matches this value. Empty when CAPI_SHARED_SECRET
        // is unset on the publish function (dev only — proxy skips check).
        pageToken: String(pageToken || ''),
        pageId: String(pageId || ''),
        product: {
            id: String(product?.id || product?.code || ''),
            name: String(product?.nameArabic || product?.name || ''),
            category: String(product?.category || ''),
            currency: String(product?.currency || ''),
            value: Number(product?.value || product?.unitPrice || 0) || 0,
        },
    };
    // The two `</script>` patterns we need to defend against:
    //   1. Literal `</script>` inside a user string (most common — e.g. a
    //      product name or test event code containing that exact sequence).
    //   2. The JSON-escaped form `<\/script>` that JSON.stringify would
    //      otherwise emit as-is — the HTML parser's raw-text scan would
    //      leave it untouched, but a future consumer that round-trips the
    //      value through JSON.parse + re-stringify would emit the dangerous
    //      `</script>` form, so escape it now to be future-proof.
    const json = JSON.stringify(safeConfig)
        .replace(/<\/(script)/gi, '<\\/$1')
        .replace(/<\/(script)[\s]/gi, '<\\/$1');
    // The `<\/script>` in the closing tag is a JS string literal escape —
    // it's the same defense pattern as the runtime config below
    // (see `</script>` -> `<\/script>` in `configJson`).
    return `<script>window.SOCTIV_TRACK_CONFIG = ${json};<\/script>`;
}

